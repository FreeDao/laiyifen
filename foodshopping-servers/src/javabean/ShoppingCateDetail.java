package javabean;


//商品详情
public class ShoppingCateDetail {
	private int cate_id;
	private String cate_name;
	private int cate_inventory;
	private int cate_sale_count;
	private String cate_description;
	private double cate_oldprice;
	private double cate_price;
	private int cate_image_id;
	private int category_id;
	private int flavor_category_id;
	private String cate_sale_info;
	private ShoppingCateImage shoppingCateImage;  
	
	

	public ShoppingCateDetail() {
		
	}
	
	public ShoppingCateDetail(int cateId, String cateName, int cateInventory,
			int cateSaleCount, String cateDescription, double cateOldprice,
			double catePrice, int cateImageId, int categoryId,
			int flavorCategoryId, String cateSaleInfo,ShoppingCateImage shoppingCateImage) {
		super();
		cate_id = cateId;
		cate_name = cateName;
		cate_inventory = cateInventory;
		cate_sale_count = cateSaleCount;
		cate_description = cateDescription;
		cate_oldprice = cateOldprice;
		cate_price = catePrice;
		cate_image_id = cateImageId;
		category_id = categoryId;
		flavor_category_id = flavorCategoryId;
		cate_sale_info = cateSaleInfo;
		this.shoppingCateImage = shoppingCateImage;
	}

	public int getCate_id() {
		return cate_id;
	}

	public void setCate_id(int cateId) {
		cate_id = cateId;
	}

	public String getCate_name() {
		return cate_name;
	}

	public void setCate_name(String cateName) {
		cate_name = cateName;
	}

	public int getCate_inventory() {
		return cate_inventory;
	}

	public void setCate_inventory(int cateInventory) {
		cate_inventory = cateInventory;
	}

	public int getCate_sale_count() {
		return cate_sale_count;
	}

	public void setCate_sale_count(int cateSaleCount) {
		cate_sale_count = cateSaleCount;
	}

	public String getCate_description() {
		return cate_description;
	}

	public void setCate_description(String cateDescription) {
		cate_description = cateDescription;
	}

	public double getCate_oldprice() {
		return cate_oldprice;
	}

	public void setCate_oldprice(double cateOldprice) {
		cate_oldprice = cateOldprice;
	}

	public double getCate_price() {
		return cate_price;
	}

	public void setCate_price(double catePrice) {
		cate_price = catePrice;
	}

	public int getCate_image_id() {
		return cate_image_id;
	}

	public void setCate_image_id(int cateImageId) {
		cate_image_id = cateImageId;
	}

	public int getCategory_id() {
		return category_id;
	}

	public void setCategory_id(int categoryId) {
		category_id = categoryId;
	}

	public int getFlavor_category_id() {
		return flavor_category_id;
	}

	public void setFlavor_category_id(int flavorCategoryId) {
		flavor_category_id = flavorCategoryId;
	}

	public String getCate_sale_info() {
		return cate_sale_info;
	}

	public void setCate_sale_info(String cateSaleInfo) {
		cate_sale_info = cateSaleInfo;
	}
	
	public ShoppingCateImage getShoppingCateImage() {
		return shoppingCateImage;
	}

	public void setShoppingCateImage(ShoppingCateImage shoppingCateImage) {
		this.shoppingCateImage = shoppingCateImage;
	}

	@Override
	public String toString() {
		// TODO Auto-generated method stub
		return "cate_name : "+cate_name; 
	}
	
	
}
