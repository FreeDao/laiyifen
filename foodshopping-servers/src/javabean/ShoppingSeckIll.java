package javabean;

public class ShoppingSeckIll {
	private int seckill_ID;
	private int cate_id;
	private long seckill_time;
	
	public ShoppingSeckIll() {
		super();
	}

	public ShoppingSeckIll(int seckill_ID, int cate_id, long seckill_time) {
		super();
		this.seckill_ID = seckill_ID;
		this.cate_id = cate_id;
		this.seckill_time = seckill_time;
	}

	public int getSeckill_ID() {
		return seckill_ID;
	}

	public void setSeckill_ID(int seckill_ID) {
		this.seckill_ID = seckill_ID;
	}

	public int getCate_id() {
		return cate_id;
	}

	public void setCate_id(int cate_id) {
		this.cate_id = cate_id;
	}

	public long getSeckill_time() {
		return seckill_time;
	}

	public void setSeckill_time(long seckill_time) {
		this.seckill_time = seckill_time;
	}

	@Override
	public String toString() {
		return "ShoppingSeckIll [seckill_ID=" + seckill_ID + ", cate_id="
				+ cate_id + ", seckill_time=" + seckill_time + "]";
	}
	
	
}
