package com.com.bean;

//商品评论表
public class ShoppingCateComment { 
	int cate_comment_id; //评论id
	int cate_id;	//商品id
	String cate_comment_date; //评论日期
 	String cate_user_name; 	//评论人
	String cate_comment_context; //评论内容
	int cate_star_level; //评论星级
	
	public ShoppingCateComment (){
		
	}
	
	public ShoppingCateComment(int cate_comment_id, int cate_id,
			String cate_comment_date, String cate_user_name,
			String cate_comment_context, int cate_star_level) {
		super();
		this.cate_comment_id = cate_comment_id;
		this.cate_id = cate_id;
		this.cate_comment_date = cate_comment_date;
		this.cate_user_name = cate_user_name;
		this.cate_comment_context = cate_comment_context;
		this.cate_star_level = cate_star_level;
	}

	public int getCate_comment_id() {
		return cate_comment_id;
	}

	public void setCate_comment_id(int cate_comment_id) {
		this.cate_comment_id = cate_comment_id;
	}

	public int getCate_id() {
		return cate_id;
	}

	public void setCate_id(int cate_id) {
		this.cate_id = cate_id;
	}

	public String getCate_comment_date() {
		return cate_comment_date;
	}

	public void setCate_comment_date(String cate_comment_date) {
		this.cate_comment_date = cate_comment_date;
	}

	public String getCate_user_name() {
		return cate_user_name;
	}

	public void setCate_user_name(String cate_user_name) {
		this.cate_user_name = cate_user_name;
	}

	public String getCate_comment_context() {
		return cate_comment_context;
	}

	public void setCate_comment_context(String cate_comment_context) {
		this.cate_comment_context = cate_comment_context;
	}

	public int getCate_star_level() {
		return cate_star_level;
	}

	public void setCate_star_level(int cate_star_level) {
		this.cate_star_level = cate_star_level;
	}
	
	
	
}
